#[cfg(test)]
mod tests {
    use super::*;
    use crate::teacher::Problem;

    fn test_problem() -> Problem {
        Problem {
            id: 1,
            statement: "Prove that 0 + n = n".to_string(),
            level: 1,
            tags: vec!["arithmetic".to_string()],
            building_blocks: vec![],
            difficulty: 0.3,
        }
    }

    #[test]
    fn student_creates_with_zero_stats() {
        let s = Student::new(0, StudentConfig::default(), 42);
        assert_eq!(s.id, 0);
        assert_eq!(s.problems_attempted, 0);
        assert_eq!(s.problems_solved, 0);
        assert!((s.success_rate() - 0.0).abs() < 1e-6);
    }

    #[test]
    fn npsi_noise_correct_size() {
        let mut s = Student::new(0, StudentConfig::default(), 42);
        let sizes = vec![100, 200, 50];
        let noise = s.generate_noise(3, &sizes, 1);
        assert_eq!(noise.len(), 3);
        assert_eq!(noise[0].len(), 100);
        assert_eq!(noise[1].len(), 200);
        assert_eq!(noise[2].len(), 50);
    }

    #[test]
    fn npsi_noise_scales_with_level() {
        let config = StudentConfig {
            base_noise: 1.0,
            ..Default::default()
        };
        let mut s1 = Student::new(0, config.clone(), 42);
        let mut s2 = Student::new(1, config, 42);
        let sizes = vec![1000];
        let n1 = s1.generate_noise(1, &sizes, 1);
        let n2 = s2.generate_noise(1, &sizes, 10);

        let var1: f32 = n1[0].iter().map(|v| v * v).sum::<f32>() / 1000.0;
        let var2: f32 = n2[0].iter().map(|v| v * v).sum::<f32>() / 1000.0;
        // Level 10 should have higher variance than level 1
        assert!(var2 > var1, "var2={var2} should be > var1={var1}");
    }

    #[test]
    fn attempt_produces_solution() {
        let mut s = Student::new(0, StudentConfig::default(), 42);
        let problem = test_problem();
        let bytes = b"theorem t : 0 + n = n := by simp".to_vec();
        let logits = vec![vec![0.0f32; 256]; bytes.len()];
        let targets: Vec<u8> = bytes.clone();
        let attempt = s.attempt_problem(&problem, bytes, &logits, &targets);
        assert_eq!(attempt.student_id, 0);
        assert_eq!(attempt.problem_id, 1);
        assert!(!attempt.generated_text.is_empty());
        assert_eq!(s.problems_attempted, 1);
    }

    #[test]
    fn log_prob_correct_for_uniform() {
        // Uniform logits over 256 classes → log(1/256)
        let logits = vec![0.0f32; 256];
        let lp = compute_log_prob(&logits, 42);
        let expected = -(256.0f32).ln();
        assert!((lp - expected).abs() < 1e-4, "lp={lp} expected={expected}");
    }

    #[test]
    fn log_prob_peaked() {
        // Peak at target
        let mut logits = vec![0.0f32; 256];
        logits[10] = 10.0;
        let lp = compute_log_prob(&logits, 10);
        assert!(
            lp > -0.1,
            "peaked logits should give high log prob, got {lp}"
        );
    }

    #[test]
    fn extract_tactics_finds_known() {
        let text = "by simp [add_comm]; ring";
        let tactics = extract_tactics(text);
        assert!(tactics.contains(&"simp".to_string()));
        assert!(tactics.contains(&"ring".to_string()));
    }

    #[test]
    fn success_rate_tracks() {
        let mut s = Student::new(0, StudentConfig::default(), 42);
        s.problems_attempted = 4;
        s.problems_solved = 3;
        assert!((s.success_rate() - 0.75).abs() < 1e-6);
    }
}
